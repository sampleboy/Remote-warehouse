public class BreakAndContinue {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            if (i % 3 == 0) {
                //3\6\9
                //break;
                continue;
            }
            System.out.println("zhangpengwei");
        }
    }
}
